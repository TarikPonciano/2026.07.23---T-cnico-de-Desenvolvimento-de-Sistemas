# Construa um programa que recebe dois números e imprime a soma desses números. 
# Melhore o programa para exibir a subtração, a multiplicação e a divisão dos números

print("Bem vindo à calculadora XYZ")

num1 = float(input("Digite o primeiro número:"))

num2 = float(input("Digite o segundo número:"))

soma = num1 + num2
subtracao = num1 - num2
multiplicacao = num1 * num2
divisao = num1 / num2

print(f"Resultado da Soma: {soma}")
print(f"Resultado da Subtração: {subtracao}")
print(f"Resultado da Multiplicação: {multiplicacao}")
print(f"Resultado da Divisão: {divisao}")

